package es.lda.core.exception;

/**
 *
 * @author Jose Antonio
 */
public class STVException extends DriverException {

    /**
     * Creates a new instance of <code>STVException</code> without detail message.
     */
    public STVException() {
    }

    /**
     * Constructs an instance of <code>STVException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public STVException(String msg) {
        super(msg);
    }
}
